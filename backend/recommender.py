def _sample_candidates(n_per_industry: int = 3, seed: int = None,
                       industry_filter: str = None,
                       full_stock_cache: list = None,
                       sample_size: int = None) -> list:
    """
    采样候选股票池

    优先从 full_stock_cache（全量 A 股，约 5000 只）中随机抽取：
      - 指定行业：按证监会行业代码过滤后随机抽 TARGET 只（不足则全取）
      - 不指定行业：全量随机抽 TARGET 只，按行业均匀分布

    full_stock_cache 格式：[{"代码": "000001", "名称": "平安银行",
                              "市场": "A股", "industry": "J66货币金融服务"}, ...]
    若 full_stock_cache 为空或无行业字段，则回退到精选池。
    sample_size: 用户指定的抽样数量（20~200），None 时默认 100。
    """
    if seed is not None:
        random.seed(seed)
    else:
        import time
        random.seed(int(time.time() / 60))

    # 目标候选数：用户指定则用之，否则默认 100，限制在 20~200
    TARGET = max(20, min(200, int(sample_size))) if sample_size is not None else 100

    # ── 判断缓存是否含行业字段 ──────────────────────
    cache_has_industry = (
        full_stock_cache and
        len(full_stock_cache) > 100 and
        full_stock_cache[0].get("industry")
    )
    # ── 指定行业模式 ─────────────────────────────────
    if industry_filter:
        if cache_has_industry:
            # 从全量缓存按证监会行业代码过滤
            csrc_codes = _SW_TO_CSRC.get(industry_filter, [])
            # 取前缀匹配（如 "C39" 匹配 "C39计算机..."）
            pool = [
                (item["代码"], item["名称"])
                for item in full_stock_cache
                if item.get("市场") == "A股" and
                   any(item.get("industry", "").startswith(c) for c in csrc_codes)
            ]
            # 若证监会分类命中太少（<20），补充精选池
            fallback = INDUSTRY_POOL.get(industry_filter, [])
            fallback_codes = {c for c, _ in pool}
            for code, name in fallback:
                if code not in fallback_codes:
                    pool.append((code, name))
                    fallback_codes.add(code)
        else:
            # 无行业字段，回退精选池
            pool = list(INDUSTRY_POOL.get(industry_filter, []))

        # 去重
        seen = set()
        unique = []
        for code, name in pool:
            if code not in seen:
                seen.add(code)
                unique.append((code, name))

        # 精选池不足 TARGET 时，从全量缓存按关键词补充
        if len(unique) < TARGET and cache_has_industry:
            keywords = _INDUSTRY_KEYWORDS.get(industry_filter, [])
            existing_codes = {c for c, _ in unique}
            for item in full_stock_cache:
                if item.get("市场") != "A股":
                    continue
                name_str = item.get("名称", "")
                if any(kw in name_str for kw in keywords):
                    c, n = item["代码"], item["名称"]
                    if c not in existing_codes:
                        unique.append((c, n))
                        existing_codes.add(c)
                if len(unique) >= TARGET * 2:  # 最多补到200，再随机抽
                    break

        if len(unique) <= TARGET:
            sampled = unique
        else:
            sampled = random.sample(unique, TARGET)

        return [(code, name, industry_filter) for code, name in sampled]

    # ── 全行业模式：全量随机抽 TARGET 只 ────────────
    if cache_has_industry:
        a_stocks = [(item["代码"], item["名称"])
                    for item in full_stock_cache if item.get("市场") == "A股"]
        if len(a_stocks) >= TARGET:
            sampled = random.sample(a_stocks, TARGET)
            return [(code, name, "全行业") for code, name in sampled]

    # 回退：精选池各行业各抽 n_per_industry 只
    candidates = []
    for industry, stocks in INDUSTRY_POOL.items():
        seen = set()
        unique = []
        for code, name in stocks:
            if code not in seen:
                seen.add(code)
                unique.append((code, name))
        k = min(n_per_industry, len(unique))
        sampled = random.sample(unique, k)
        candidates.extend([(code, name, industry) for code, name in sampled])
    return candidates


def _calc_rsi(prices: np.ndarray, period: int = 14) -> float:
    """计算 RSI"""
    if len(prices) < period + 1:
        return 50.0
    deltas = np.diff(prices)
    gains = np.where(deltas > 0, deltas, 0)
    losses = np.where(deltas < 0, -deltas, 0)
    avg_gain = np.mean(gains[-period:])
    avg_loss = np.mean(losses[-period:])
    if avg_loss == 0:
        return 100.0
    rs = avg_gain / avg_loss
    return 100 - 100 / (1 + rs)


def _fetch_index_return(days: int = 20) -> float:
    """
    获取沪深300近N日涨跌幅（用于计算相对强弱）
    失败时返回0（不影响其他因子）
    """
    try:
        end = pd.Timestamp.today()
        start = end - pd.Timedelta(days=days + 10)
        lg = bs.login()
        rs = bs.query_history_k_data_plus(
            "sh.000300",
            "date,close",
            start_date=start.strftime("%Y-%m-%d"),
            end_date=end.strftime("%Y-%m-%d"),
            frequency="d",
        )
        rows = []
        while rs.error_code == "0" and rs.next():
            row = rs.get_row_data()
            if row[1]:
                rows.append(float(row[1]))
        bs.logout()
        if len(rows) >= 20:
            return (rows[-1] - rows[-20]) / rows[-20] * 100
    except Exception:
        pass
    return 0.0


def _score_stock_from_df(df: pd.DataFrame, symbol: str, name: str, industry: str,
                         index_return_20d: float = 0.0) -> dict | None:
    """
    对单只股票计算多因子综合评分（原始分，后续截面归一化）
    接收已拉取好的 DataFrame，不再自己拉数据
    返回原始因子值，不做归一化（归一化在截面层面做）
    """
    try:
        if df is None or len(df) < 30:
            return None

        close = df["close"].values
        volume = df["volume"].values if "volume" in df.columns else np.ones(len(df))
        last = close[-1]
        n = len(close)

        # ── F1：动量（近20日涨跌幅）──────────────────
        momentum_20 = (close[-1] - close[-20]) / close[-20] * 100

        # ── F2：相对强弱（个股 vs 大盘）──────────────
        # 个股跑赢大盘越多，得分越高
        relative_strength = momentum_20 - index_return_20d

        # ── F3：趋势加速度（近5日 vs 近20日日均）──────
        # 近5日日均涨幅 > 近20日日均涨幅 → 加速上涨
        ret_5d  = (close[-1] - close[-6]) / close[-6] * 100 / 5   # 日均
        ret_20d = momentum_20 / 20                                  # 日均
        acceleration = ret_5d - ret_20d   # 正值=加速，负值=减速

        # ── F4：均线多头排列程度 ──────────────────────
        ma5  = np.mean(close[-5:])
        ma10 = np.mean(close[-10:])
        ma20 = np.mean(close[-20:])
        # 三条均线都多头排列得满分，否则按比例
        ma_bull = 0
        if ma5 > ma10:  ma_bull += 1
        if ma10 > ma20: ma_bull += 1
        if close[-1] > ma5: ma_bull += 1
        # 额外：价格偏离MA20的程度（适度偏离好，过度偏离差）
        ma20_dev = (last - ma20) / ma20 * 100  # -10%~+10%

        # ── F5：波动率（越低越稳定）──────────────────
        returns_20 = np.diff(close[-21:]) / close[-21:-1]
        vol_20 = np.std(returns_20) * 100  # 日波动率%

        # ── F6：量价配合 ──────────────────────────────
        price_chg_5 = close[-1] - close[-6]
        vol_avg_5   = np.mean(volume[-5:])
        vol_avg_pre = np.mean(volume[-10:-5])
        vol_ratio   = vol_avg_5 / (vol_avg_pre + 1e-9)  # 近5日量 vs 前5日量
        # 涨时放量 or 跌时缩量 → 量价配合
        vol_price_ok = (price_chg_5 > 0 and vol_ratio > 1.0) or \
                       (price_chg_5 < 0 and vol_ratio < 1.0)

        # ── F7：RSI ───────────────────────────────────
        rsi = _calc_rsi(close)

        # 近5日涨跌幅（展示用）
        change_5d = (close[-1] - close[-6]) / close[-6] * 100

        return {
            "symbol": symbol,
            "name": name,
            "industry": industry,
            "last_price": round(last, 2),
            "change_5d": round(change_5d, 2),
            "momentum_20d": round(momentum_20, 2),
            "rsi": round(rsi, 1),
            "vol_daily": round(vol_20, 2),
            # 原始因子值（用于截面归一化）
            "_f1_momentum":     momentum_20,
            "_f2_rs":           relative_strength,
            "_f3_accel":        acceleration,
            "_f4_ma_bull":      ma_bull,
            "_f4_ma20_dev":     ma20_dev,
            "_f5_vol":          vol_20,
            "_f6_vol_price_ok": vol_price_ok,
            "_f7_rsi":          rsi,
        }
    except Exception:
        return None


# ─────────────────────────────────────────
# 绝对值评分函数（核心改进）
# ─────────────────────────────────────────

def _abs_f1(momentum: float) -> float:
    """F1 动量(20日) → 绝对分 0~100"""
    if   momentum >  10: return 100.0
    elif momentum >   5: return 80.0
    elif momentum >   0: return 60.0
    elif momentum >  -5: return 40.0
    elif momentum > -10: return 20.0
    else:                return 0.0

def _abs_f3(accel: float) -> float:
    """F3 趋势加速度 → 绝对分 0~100"""
    if   accel >  0.8: return 100.0
    elif accel >  0.3: return 85.0
    elif accel >  0.0: return 60.0
    elif accel > -0.5: return 30.0
    else:              return 0.0

def _abs_f4(ma_bull: int, ma20_dev: float) -> float:
    """F4 均线多头排列 → 绝对分 0~100"""
    base = ma_bull * 30.0          # 0 / 30 / 60 / 90
    dev_abs = abs(ma20_dev)
    if   dev_abs <= 2:  dev_bonus = 10.0
    elif dev_abs <= 5:  dev_bonus = 7.0
    elif dev_abs <= 10: dev_bonus = 3.0
    else:               dev_bonus = 0.0
    return min(100.0, base + dev_bonus)

def _abs_f5(vol: float) -> float:
    """F5 波动率 → 绝对分 0~100（越低越好）"""
    if   vol < 1.0: return 100.0
    elif vol < 2.0: return 80.0
    elif vol < 3.0: return 50.0
    elif vol < 4.0: return 20.0
    else:           return 0.0

def _abs_f7(rsi: float) -> float:
    """F7 RSI → 绝对分 0~100"""
    if   40 <= rsi <= 65: return 100.0
    elif 30 <= rsi <  40: return 60.0
    elif 65 <  rsi <= 75: return 60.0
    else:                 return 20.0


def _compute_scores(results: list, index_return_20d: float = 0.0) -> list:
    """
    混合评分：
      - F1/F3/F4/F5/F6/F7 用绝对值阈值（避免矬子里拔将军）
      - F2 相对强弱仍用截面排名百分位（必须对比才有意义）
    最终综合分 = 加权平均，0~100 的绝对意义分数
    """
    if not results:
        return results

    # F2 相对强弱：截面排名百分位
    rs_vals = np.array([r["_f2_rs"] for r in results])
    if len(rs_vals) > 1:
        f2_scores = pd.Series(rs_vals).rank(pct=True).values * 100
    else:
        f2_scores = np.array([50.0])

    weights = {"f1": 0.30, "f2": 0.20, "f3": 0.15,
               "f4": 0.15, "f5": 0.10, "f6": 0.05, "f7": 0.05}

    for i, r in enumerate(results):
        f1 = _abs_f1(r["_f1_momentum"])
        f2 = float(f2_scores[i])
        f3 = _abs_f3(r["_f3_accel"])
        f4 = _abs_f4(r["_f4_ma_bull"], r["_f4_ma20_dev"])
        f5 = _abs_f5(r["_f5_vol"])
        f6 = 70.0 if r["_f6_vol_price_ok"] else 30.0
        f7 = _abs_f7(r["_f7_rsi"])

        total = (f1 * weights["f1"] + f2 * weights["f2"] +
                 f3 * weights["f3"] + f4 * weights["f4"] +
                 f5 * weights["f5"] + f6 * weights["f6"] +
                 f7 * weights["f7"])

        score = round(float(total), 1)
        r["score"] = score
        r["factors"] = {
            "动量(20日)":  round(f1, 1),
            "相对强弱":    round(f2, 1),
            "趋势加速度":  round(f3, 1),
            "均线多头":    round(f4, 1),
            "稳定性":      round(f5, 1),
            "量价配合":    round(f6, 1),
            "RSI":         round(f7, 1),
        }
        # 原始因子值（带单位，供前端详解面板展示）
        r["factor_values"] = {
            "动量(20日)":  f"{r['_f1_momentum']:+.2f}%",
            "相对强弱":    f"{r['_f2_rs']:+.2f}%",
            "趋势加速度":  f"{r['_f3_accel']:+.3f}%/日",
            "均线多头":    f"{int(r['_f4_ma_bull'])}/3 条多头",
            "稳定性":      f"{r['_f5_vol']:.2f}%/日",
            "量价配合":    "配合" if r["_f6_vol_price_ok"] else "背离",
            "RSI":         f"{r['_f7_rsi']:.1f}",
        }
        # 绝对值信号阈值（有实际意义）
        if score >= 72:
            r["signal"] = "强烈推荐"
            r["signal_color"] = "#26c281"
        elif score >= 58:
            r["signal"] = "推荐关注"
            r["signal_color"] = "#4f8ef7"
        elif score >= 44:
            r["signal"] = "中性观望"
            r["signal_color"] = "#8892a4"
        else:
            r["signal"] = "谨慎回避"
            r["signal_color"] = "#e74c3c"

        # 清理内部字段
        for k in list(r.keys()):
            if k.startswith("_"):
                del r[k]

    return results


def get_recommendations(top_n: int = 10, pool: list = None, seed: int = None,
                        industry_filter: str = None,
                        full_stock_cache: list = None,
                        progress_cb=None,
                        sample_size: int = None) -> dict:
    """
    动态多因子选股推荐

    industry_filter: 指定行业（如"银行"），None 表示全行业随机采样
    full_stock_cache: 全量A股列表缓存（用于行业筛选时扩充候选池）
    progress_cb: 可选回调 progress_cb(step, current, total, msg)，用于 SSE 进度推送
    """
    def _cb(step, current, total, msg=""):
        if progress_cb:
            try:
                progress_cb(step, current, total, msg)
            except Exception:
                pass

    try:
        # ── 1. 采样候选池 ──────────────────────────────
        _cb("sample", 0, 1, "正在采样候选股票...")
        if pool:
            candidates = [(c, n, "其他") for c, n in pool]
        else:
            candidates = _sample_candidates(
                n_per_industry=3, seed=seed,
                industry_filter=industry_filter,
                full_stock_cache=full_stock_cache,
                sample_size=sample_size,
            )
        sampled_count = len(candidates)
        _cb("sample", 1, 1, f"采样完成，共 {sampled_count} 只候选")

        # ── 2. 获取大盘基准收益（用于相对强弱）──────────
        _cb("index", 0, 1, "获取大盘基准收益...")
        index_ret = _fetch_index_return(days=20)
        _cb("index", 1, 1, "大盘基准获取完成")

        # ── 3. 批量拉取行情（一次登录，串行查询，避免 baostock 多线程冲突）──
        results = []
        errors = []

        # 一次性拉取所有 A 股数据
        from datetime import timedelta
        end_dt = pd.Timestamp.today()
        start_dt = end_dt - timedelta(days=120)
        start_str = start_dt.strftime("%Y-%m-%d")
        end_str   = end_dt.strftime("%Y-%m-%d")

        # 批量拉取：一次 baostock 登录，循环查询
        raw_data = {}   # symbol -> DataFrame
        a_stock_candidates = [(s, n, ind) for s, n, ind in candidates if len(s) == 6]
        hk_candidates      = [(s, n, ind) for s, n, ind in candidates if len(s) == 5]

        total_fetch = len(a_stock_candidates) + len(hk_candidates)
        fetched = 0

        if a_stock_candidates:
            try:
                lg = bs.login()
                for symbol, name, industry in a_stock_candidates:
                    try:
                        bs_code = f"sh.{symbol}" if symbol.startswith("6") else f"sz.{symbol}"
                        rs = bs.query_history_k_data_plus(
                            bs_code,
                            "date,open,high,low,close,volume",
                            start_date=start_str, end_date=end_str,
                            frequency="d", adjustflag="2",
                        )
                        rows = []
                        while rs.error_code == "0" and rs.next():
                            rows.append(rs.get_row_data())
                        if rows:
                            df = pd.DataFrame(rows, columns=["date","open","high","low","close","volume"])
                            df["date"] = pd.to_datetime(df["date"])
                            df.set_index("date", inplace=True)
                            df = df.replace("", np.nan).dropna().astype(float)
                            raw_data[symbol] = df
                        else:
                            errors.append(symbol)
                    except Exception:
                        errors.append(symbol)
                    fetched += 1
                    _cb("fetch", fetched, total_fetch, f"拉取行情 {name}({symbol})")
            finally:
                bs.logout()

        # 港股单独用 HTTP 拉取
        for symbol, name, industry in hk_candidates:
            try:
                from engine import fetch_hk_stock_data
                df = fetch_hk_stock_data(symbol, start_str, end_str)
                raw_data[symbol] = df
            except Exception:
                errors.append(symbol)
            fetched += 1
            _cb("fetch", fetched, total_fetch, f"拉取行情 {name}({symbol})")

        # 用拉到的数据计算因子
        scored = 0
        total_score = len(candidates)
        for symbol, name, industry in candidates:
            df = raw_data.get(symbol)
            if df is None or len(df) < 30:
                errors.append(symbol)
                scored += 1
                _cb("score", scored, total_score, f"评分 {name}({symbol})")
                continue
            try:
                r = _score_stock_from_df(df, symbol, name, industry, index_return_20d=index_ret)
                if r:
                    results.append(r)
                else:
                    errors.append(symbol)
            except Exception:
                errors.append(symbol)
            scored += 1
            _cb("score", scored, total_score, f"评分 {name}({symbol})")

        # 去重 errors（同一只股票可能在拉取和评分两个阶段都被记录）
        errors_count = len(set(errors))

        if not results:
            return {"success": False, "error": "所有股票数据获取失败"}

        # ── 4. 混合评分（绝对值 + 相对强弱截面）──────
        results = _compute_scores(results, index_return_20d=index_ret)

        # ── 5. 排序，取 Top N ──────────────────────────
        results.sort(key=lambda x: x["score"], reverse=True)

        # 行业分散（指定行业时不限制，全行业时同行业最多2只）
        if industry_filter:
            top = results[:top_n]
        else:
            top = []
            industry_count = {}
            for r in results:
                ind = r.get("industry", "其他")
                cnt = industry_count.get(ind, 0)
                if cnt < 2:
                    top.append(r)
                    industry_count[ind] = cnt + 1
                if len(top) >= top_n:
                    break
            if len(top) < top_n:
                for r in results:
                    if r not in top:
                        top.append(r)
                    if len(top) >= top_n:
                        break

        # 统计行业分布
        industry_dist = {}
        for r in top:
            ind = r.get("industry", "其他")
            industry_dist[ind] = industry_dist.get(ind, 0) + 1

        # 评分分布统计（帮助用户理解绝对分含义）
        all_scores = [r["score"] for r in results]
        score_dist = {
            "强烈推荐(>=72)": sum(1 for s in all_scores if s >= 72),
            "推荐关注(58~72)": sum(1 for s in all_scores if 58 <= s < 72),
            "中性观望(44~58)": sum(1 for s in all_scores if 44 <= s < 58),
            "谨慎回避(<44)": sum(1 for s in all_scores if s < 44),
        }

        filter_desc = (
            f"行业「{industry_filter}」随机抽取{sampled_count}只"
            if industry_filter
            else f"全行业随机采样{sampled_count}只"
        )
        scored_count = len(results)
        fail_note = f"，{errors_count}只数据不足/获取失败" if errors_count > 0 else ""

        return {
            "success": True,
            "sampled_count": sampled_count,
            "scored_count": scored_count,
            "errors_count": errors_count,
            "total_scanned": scored_count,   # 保持向后兼容
            "top_n": len(top),
            "results": top,
            "industry_dist": industry_dist,
            "score_dist": score_dist,
            "index_return_20d": round(index_ret, 2),
            "industry_filter": industry_filter or "全行业",
            "update_time": pd.Timestamp.today().strftime("%Y-%m-%d %H:%M"),
            "algo_note": (
                filter_desc + f"候选股（成功评分{scored_count}只{fail_note}），"
                + f"沪深300近20日{'+' if index_ret>=0 else ''}{index_ret:.1f}%，"
                + f"绝对值评分后取Top{top_n}"
                + ("" if industry_filter
                   else "（同行业最多2只）")
            ),
        }

    except Exception as e:
        return {"success": False, "error": str(e), "traceback": traceback.format_exc()}
